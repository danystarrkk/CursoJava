// Incrementos


let puntaje = 10;

console.log(puntaje++); // primero imprime el valor y luego incrementa
console.log(++puntaje); // primero incrementa y luego imprime


// Decremento

console.log(puntaje--); // primero imprime el valor y luego decrementa
console.log(--puntaje); //primero decrementa el valor y despues lo imprime

// Incrementos Especificos

puntaje += 10;
console.log(puntaje);


// Decremento Especificos

puntaje -= 10;
console.log(puntaje);

